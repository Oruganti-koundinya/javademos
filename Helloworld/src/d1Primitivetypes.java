
public class d1Primitivetypes {

	public static void main(String[] args) {
		byte byteminvalue = Byte.MIN_VALUE;
		byte bytemaxvalue = Byte.MAX_VALUE;
		System.out.println("Minimum Value of Byte =" + byteminvalue);
		System.out.println("Maximum Value of Byte =" + bytemaxvalue);

		short minshortvalue = Short.MIN_VALUE;
		short maxshortvalue = Short.MAX_VALUE;
		System.out.println("Minimum Value of Short =" + minshortvalue);
		System.out.println("Maximum Value of Byte =" + maxshortvalue);

		int minintvalue = Integer.MIN_VALUE;
		int maxintvalue = Integer.MAX_VALUE;
		System.out.println("Minimum Integer Value =" + minintvalue);
		System.out.println("Maximum Integer Value =" + maxintvalue);

		long minlongvalue = Long.MIN_VALUE;
		long maxlongvalue = Long.MAX_VALUE;
		System.out.println("Minimum value of long =" + minlongvalue);
		System.out.println("Maximum value of long =" + maxlongvalue);

		float minfloatvalue = Float.MIN_VALUE;
		float maxfloatvalue = Float.MAX_VALUE;
		System.out.println("Minimum Value of Float=" + minfloatvalue);
		System.out.println("Maximum Value of Float=" + maxfloatvalue);

		double mindoublevalue = Double.MIN_VALUE;
		double maxdoublevalue = Double.MAX_VALUE;
		System.out.println("Minimum Value of double=" + mindoublevalue);
		System.out.println("Maximum Value of double=" + maxdoublevalue);

		int val = 5;
		float val1 = 5.25f;
		double val2 = 5.25;
		System.out.println("IntValue =" + val + ",Floatval =" + val1 + ",Doubleval =" + val2);

	}

}
